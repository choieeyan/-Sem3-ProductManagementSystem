package Util;


import java.util.UUID;
/**
 *
 * @author Kum Hoe
 */
public class Function {
    
     public static Long generateUniqueId() {
        long val = -1;
        do {
            val = UUID.randomUUID().getMostSignificantBits();
        } while (val < 0);
        return val;
    }
   
}

