    public ProductManagementUI() {
        initComponents();
        model = (DefaultTableModel)jTable1.getModel();
        model.setColumnIdentifiers(
                new String[]{"Product ID","Product Name","Brand","Description","Quantity","Price","Supplier Info","Category"});
        TextAccess pda  = new TextAccess();
        pda.setFileName("product.txt");
        ArrayList<String> list = pda.readAll();
        for (String s : list) {
            String sParts[] = s.split("\\|"); 
            model.addRow(new String[]{sParts[0],sParts[1],sParts[2],sParts[3],sParts[4],sParts[5],sParts[6],sParts[7]});
        }
    }

